a=10
b=7

#suma
suma=a+b
print("La suma es:",suma)

#resta
resta=a-b
print("La resta es: ",resta)

#multiplicacion
multiplicacion=a*b
print("La multiplicacion es :", multiplicacion)

#division
division=a/b
print("La division es: ",division)

#division entera
div=a//b
print("La division entera es :",div)

#modulo,porcentaje
modulo=a%b
print("El modulo entre a y b es: ",modulo)

#exponente
expo=a**b
print("El exponente es: ",expo)